    <div class="box"><h2><span class="h2-heading">
        TO OTHER GOVERNMENTS
    </span></h2></div>
    <ul><li>All states should support the role of the International Criminal Court in examining alleged violations of international law such as those committed in Rafah between 1 and 4 August 2014, and pressure Israeli and Palestinian authorities to co-operate fully with the Office of the Prosecutor.
    </li><li>All states should oppose punitive measures against Palestine for joining the International Criminal Court or for submitting information on Israeli violations to the Court or taking other steps to activate international justice mechanisms. Similarly, no state should use the non-application of the jurisdiction of the International Criminal Court or other international accountability mechanisms as a bargaining chip in other negotiations such as in the context of the Israeli-Palestinian “peace process”.
</li><li>Individual states should start criminal investigations in national courts, exercising universal jurisdiction, wherever there is sufficient evidence of war crimes or other crimes under international law, and seek to arrest alleged perpetrators and bring them to justice in proceedings that fully respect international fair trial standards.
</li><li>All states should suspend the transfer to Israel of arms, munitions, weapons and military equipment, until substantive steps have been taken by Israel to achieve accountability for previous violations and effective mechanisms are in place to ensure that weapons and related equipment will not be used to commit serious violations of international human rights and humanitarian law. The suspension should include all indirect exports via other countries, the transfer of military components and technologies, and any brokering, financial or logistical activities that would facilitate such transfers.</li></ul>








