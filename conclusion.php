<section id="conclusion-2" class="conclusion conclusion-2 section"><div class="container"><div class="row"><div class="col-lg-10">
    <div class="box"><h2><span class="h2-heading">
        TO THE ISRAELI AUTHORITIES
    </span></h2></div>
    <ul><li>The Israeli authorities should co-operate fully with the preliminary examination by the Prosecutor of the International Criminal Court and any future investigations or prosecutions.
    </li><li>They should reform their domestic investigations system for allegations of international humanitarian law violations to ensure that it is independent, effective, prompt and transparent. As a first step, the Israeli authorities should fully implement the recommendations of the Turkel Commission regarding Israel’s mechanisms for investigating allegations of violations of international law. 
</li><li>They should ensure that the Israeli military revises its doctrine and tactics for fighting in densely populated areas such as Gaza so that they fully adhere to international humanitarian law, in particular the prohibition of indiscriminate and disproportionate attacks, and the requirement to take precautions in attack. This should include ordering that artillery and mortars are never used in the vicinity of civilian concentrations.
</li><li>They should allow human rights organizations access to Gaza to investigate these and other suspected violations of international law by all parties to the conflict.
</li><li>They should provide full reparation and access to an effective remedy to all victims of violations and their families, including those whose homes and property were unlawfully destroyed or damaged during attacks.
</li><li>They should immediately and fully lift the blockade imposed on Gaza since 2007. 
</li><li>They should accede to the Rome Statute of the International Criminal Court and issue a declaration accepting the International Criminal Court’s jurisdiction since 1 July 2002.</li></ul>
</div></div></div></section>


