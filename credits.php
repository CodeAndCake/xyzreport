<div class="h3-heading-box">
  <h3><span class="h3-heading">
MEDIA ANALYSIS
</span></h3>
</div>
<p>
Forensic Architecture scanned a large number of Arabic, Hebrew and English-language sources, many of which were also consulted by Amnesty International. They included Palestinian and Israeli media sources, Palestinian social media sources and photographs and video clips from photographic agencies and image banks.<sup><a data-placement="top" data-toggle="popover" data-trigger="focus" tabindex="0" role="button" data-content="Key sources include: Palestinian media sources such as Ma’an, <i>Al-Rai</i>, Al Watan Voice, Quds Radio 102.7 FM; Palestinian social media sources, including Twitter and Facebook accounts; Israeli media sites such as Walla, NRG, Ynet and <i>Haaretz</i>, Israeli TV Channel 2 and Channel 10 (many of these sources included interviews with Israeli military staff, which is subject to censorship by the IDF and has been independently corroborated when possible); photographs and video clips from photographic agencies and image banks such as Getty, Reuters, Corbis and Anadolu Agency, as well as from AP, ITN and CBS." data-html="true"><span class="glyphicon glyphicon-bookmark"></span></a></sup> They also included official Israeli and Palestinian statements appearing on IDF and Hamas websites; accounts by Palestinian NGOs, such as the Al Mezan Center for Human Rights, the Palestinian Centre for Human Rights (PCHR) and Al-Haq; testimonies given by Israeli soldiers to Breaking the Silence and published on their website; video recordings from the local ambulance services in Gaza; hospital logs which document calls to the emergency office; photographs and videos provided by civilians and activists who witnessed events. Forensic Architecture extracted further written, filmed and photographic materials from these sources.  
</p>
<div class="h3-heading-box">
  <h3><span class="h3-heading">
VIDEO TO SPACE
</span></h3>
</div>
<p>
While a few independently shot videos were uploaded onto social media during the 2008-9 Gaza/Israel conflict, Forensic Architecture retrieved a comparatively large amount of audiovisual material on social media in relation to the 2014 Gaza/Israel conflict. Photographs and videos taken by both journalists and citizens captured large amounts of spatial information about environments in which events were unfolding, including the architectural layout of sites, the location and the time of day. Video stills were collaged together to create a panoramic view of space. Working back from distinctive architectural features – for example, a water tower, a high building, a crossroads or a football pitch – Forensic Architecture located still images within the satellite images and three-dimensional models.  
</p>
<div class="img-box fit video">
<div class="embed-responsive embed-responsive-16by9"><iframe class="embed-responsive-item" src="https://player.vimeo.com/video/130710400"></iframe></div>
<span class="caption">
